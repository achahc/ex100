import java.util.Scanner;

public class ex100_8 {
    public static void main(String[] args) {
        Scanner scanner = new Scanner(System.in);

        // 문자 한 개를 입력받습니다.
        String input = scanner.nextLine();

        // 입력받은 문자를 그대로 출력합니다.
        System.out.println(input);
    }
}
